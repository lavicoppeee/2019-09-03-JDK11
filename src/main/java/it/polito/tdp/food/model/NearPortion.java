package it.polito.tdp.food.model;

public class NearPortion {

	String p;
	double peso;
	
	public NearPortion(String p, double peso) {
		super();
		this.p = p;
		this.peso = peso;
	}
	
	public String getP() {
		return p;
	}
	public void setP(String p) {
		this.p = p;
	}
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	
}
